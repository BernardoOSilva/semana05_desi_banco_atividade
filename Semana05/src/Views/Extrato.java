    private void carregarExtrato(){
        //List<> listaTransactions = makeDAOTransaction.buscarTodasAsTransacoes();
        ArrayList<Transaction> listaTransactions = new ArrayList();
        DefaultTableModel table = (DefaultTableModel)this.Tabela.getModel();
        
        for(Transaction item: listaTransactions){
            Object[] novaLinha = {item.getId(), item.getData(), item.getTipoTransacao(), item.getValor(), item.getConta_id()};
            table.addRow(novaLinha);
        }
    }

